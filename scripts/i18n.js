function getLanguageCode() {
  return typeof AppState !== "undefined" && AppState.settings?.language === "Engels" ? "en" : "nl";
}

function t(key, params = {}) {
  const dictionary = translations[getLanguageCode()] || translations.nl;
  const template = dictionary[key] || translations.nl[key] || key;
  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, value), template);
}

function setText(selector, key) {
  document.querySelectorAll(selector).forEach((element) => {
    element.textContent = t(key);
  });
}

function setPlaceholder(selector, key) {
  document.querySelectorAll(selector).forEach((element) => {
    element.setAttribute("placeholder", t(key));
  });
}

function setAria(selector, key) {
  document.querySelectorAll(selector).forEach((element) => {
    element.setAttribute("aria-label", t(key));
  });
}

function setLabel(controlSelector, key) {
  document.querySelectorAll(controlSelector).forEach((control) => {
    const label = control.closest("label");
    if (!label) return;
    const span = label?.querySelector(":scope > span");
    if (span) {
      span.textContent = t(key);
      return;
    }
    const node = [...label.childNodes].find((child) => child.nodeType === Node.TEXT_NODE);
    if (node) node.textContent = t(key);
  });
}

function setOption(selectSelector, value, key) {
  const option = document.querySelector(`${selectSelector} option[value="${value}"]`);
  if (option) option.textContent = t(key);
}

function setIconButton(selector, icon, key) {
  document.querySelectorAll(selector).forEach((button) => {
    button.innerHTML = `<span class="nav-icon" aria-hidden="true">${icon}</span>${t(key)}`;
  });
}

